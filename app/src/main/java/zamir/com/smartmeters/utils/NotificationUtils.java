    private void setNotificationMessageToSharedPrefence(String message) {
        SharedPreferences pref = mContext.getSharedPreferences(Config.SHARED_NOTIFICATIONS, 0);
        SharedPreferences.Editor editor = pref.edit();
        editor.putString(Config.SHARED_NOTIFICATIONS_MESSAGE, message);
        editor.commit();
    }

    private void addNotificationToData(String message) {
        FirebaseAuth mAuth = FirebaseAuth.getInstance();
        FirebaseDatabase database = FirebaseDatabase.getInstance();
        DatabaseReference myRef = database.getReference("notifications").child(mAuth.getCurrentUser().getUid());
        myRef.setValue(message);
    }
